{
    const choices = ['r','p','s'];

    const pc = prompt("enter the choice(r,p,s): ")

    if(!choices.includes(pc))
    {
        alert("enter a  valid option (r,p,s)")
        return
    }

    const cc = choices[Math.floor(Math.random()*3)]

    const res = cc == pc ? "tie" : pc === 'r' && cc ==='s' || pc == 'p' && cc === 'r' || pc === 's' && cc === 'p' ? "player wins" : "computer wins"
    console.log(`Player choices: ${pc}`);
    console.log(`Computers: ${cc}`);
    

    console.log(res);
    
}

